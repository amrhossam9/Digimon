    private void bActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bActionPerformed
        String m = mid.getText();
        try {
            st = c.prepareStatement("SELECT * FROM MEMBERS WHERE MEMBER_ID = ?");
            st.setString(1, m);
            rs = st.executeQuery();
            while(rs.next()){
            if(Integer.parseInt(rs.getString("FAN_C")) == 0){
                st = c.prepareStatement("UPDATE MEMBERS SET FAN_1 = ? WHERE MEMBER_ID = ?");
                st.setString(1, cid.get(tc.getSelectedIndex()));
                st.setString(2, m);
                st.executeUpdate();
                JOptionPane.showMessageDialog(this, "JOINED SUCCESSFULLY!","JOINED",1);
                st = c.prepareStatement("UPDATE MEMBERS SET FAN_C = ? WHERE MEMBER_ID = ?");
                st.setString(1, "1");
                st.setString(2, m);
                st.executeUpdate();
            }else if(Integer.parseInt(rs.getString("FAN_C")) == 1 && !(cid.get(tc.getSelectedIndex()).equals(rs.getString("FAN_1")))){
                st = c.prepareStatement("UPDATE MEMBERS SET FAN_2 = ? WHERE MEMBER_ID = ?");
                st.setString(1, cid.get(tc.getSelectedIndex()));
                st.setString(2, m);
                st.executeUpdate();
                JOptionPane.showMessageDialog(this, "JOINED SUCCESSFULLY!","JOINED",1);
                st = c.prepareStatement("UPDATE MEMBERS SET FAN_C = ? WHERE MEMBER_ID = ?");
                st.setString(1, "2");
                st.setString(2, m);
                st.executeUpdate();
            }else if(Integer.parseInt(rs.getString("FAN_C")) == 2 && !(cid.get(tc.getSelectedIndex()).equals(rs.getString("FAN_1"))) && !(cid.get(tc.getSelectedIndex()).equals(rs.getString("FAN_2")))){
                st = c.prepareStatement("UPDATE MEMBERS SET FAN_3 = ? WHERE MEMBER_ID = ?");
                st.setString(1, cid.get(tc.getSelectedIndex()));
                st.setString(2, m);
                st.executeUpdate();
                JOptionPane.showMessageDialog(this, "JOINED SUCCESSFULLY!","JOINED",1);
                st = c.prepareStatement("UPDATE MEMBERS SET FAN_C = ? WHERE MEMBER_ID = ?");
                st.setString(1, "3");
                st.setString(2, m);
                st.executeUpdate();
            }else if(Integer.parseInt(rs.getString("FAN_C")) > 2){
                JOptionPane.showMessageDialog(this, "YOU REACHED THE MAXIMUM JOINING TO THE CLUBS!","ERROR",2);
            }else{
                JOptionPane.showMessageDialog(this, "YOU ARE ALREADY A MEMBER OF ("+tc.getSelectedItem().toString()+") CLUB","ERROR",2);
            }
           
            }
        } catch (SQLException ex) {
            Logger.getLogger(jfc.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_bActionPerformed

