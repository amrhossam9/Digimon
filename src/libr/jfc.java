    public void UpdateC(){
        try {
            tc.removeAllItems();
            cid.clear();
            st = c.prepareStatement("SELECT * FROM FANCLUB");
            rs = st.executeQuery();
            int co = 0;
            while(rs.next()){
                if(Integer.parseInt(rs.getString("CLUB_ID")) != 1){
                tc.addItem(rs.getString("CLUB_NAME"));
                cid.add(co, rs.getString("CLUB_ID"));
                co++;
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(jfc.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
