    private void editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editActionPerformed
        String ID = jf2.getText();
        try{
               Integer.parseInt(ID);
            }catch (NumberFormatException e){
                JOptionPane.showMessageDialog(this, "Enter the ID in Integers!","Error",2);
            }
        try {
            st = c.prepareStatement("SELECT * FROM BOOKS WHERE BOOK_ID = ?");
            st.setString(1, ID);
            rs = st.executeQuery();
            co = 0;
            while(rs.next()){
                bedit.setVisible(true);
                next.setVisible(true);
                bedit.setBounds(200, 200, 1035, 700) ;
                co++ ;
            }
            if (co == 0)
            {
                JOptionPane.showMessageDialog(this, "Book is not Found");
            }
        }catch (SQLException ex) {
            Logger.getLogger(ubi.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_editActionPerformed

