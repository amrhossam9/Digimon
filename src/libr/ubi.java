    public void updateBP(){
        try {
                    bp1.clear();
                    st = c.prepareStatement("SELECT * FROM BOOK_Publisher");
                    rs = st.executeQuery();
                    int co = 0; 
                    while(rs.next()){
                        bpcb.addItem(rs.getString("PUBLISHER"));
                        bp1.add(co,rs.getString("PUBLISHER_ID"));
                        co++;
                    }
                    
                } catch (SQLException ex) {
                    Logger.getLogger(ubi.class.getName()).log(Level.SEVERE, null, ex);
                }
    }
